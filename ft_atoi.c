int ft_atoi(const char *str)
{
    int i;
    int j;
    int k;
    int n;

    i = 0;
    j = 0;
    k = 1;
    n = 0;
    while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
        i++;
    if (str[i] == '-' || str[i] == '+')
    {    
        if (str[i] == '-')
            j++;  
        i++;
    }
    while (str[i] >= 48 && str[i] <= 57)
    {
        n = (n * k) + (str[i] - 48);
        k *= 10;
        i++;
    } 
    if (j)
        n = -n;
    return (n);
}